// CONFIGURACION REGISTROS
.DEF C = R16 ; CONTADOR
.DEF S7 = R17 ; PUERTO SIETE SEGMENTOS
.DEF L = R18 ; RUTINA LIBRE
.DEF D = R20 ; PUERTO PARA EL DELAY
.DEF CD = R21 ; PUERTO CICLO DELAY
.DEF DD = R22 ; PUERTO DELAY DOS

// CONFIGURACION DE PUERTOS
// PUERTO B SALIDA
SER C
OUT DDRB, C
// PUERTO D SALIDA
OUT DDRD, C

FIN:
SBIC PINA, 0
CALL ASC
SBIC PINA, 1
CALL INCISOB
SBIC PINA, 2
CALL INCISOC
RJMP FIN

// INCISO A
ASC:
OUT PORTD, C
RCALL D250
INC C
CPI C, 9
BREQ DESC
RJMP ASC
DESC:
OUT PORTD, C
DEC C
RCALL D250
CPI C, 0
BREQ ASC
RJMP DESC
RET
// FIN INCISO A
